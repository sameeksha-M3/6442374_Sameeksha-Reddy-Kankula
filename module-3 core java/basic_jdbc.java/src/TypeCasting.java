public class TypeCasting {
    public static void main(String[] args) {
        int a=10;
        double b=a;
        System.out.println("converting integer to double");
        System.out.println("integer value is "+a);
        System.out.println("After converting double value is "+b);

        System.out.println("COnverting double to integer");
        System.out.println("double to integer "+(int)b);
    }
}
